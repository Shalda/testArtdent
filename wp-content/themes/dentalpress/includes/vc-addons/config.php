	if( !function_exists('dentalpress_vc_before_init') ){
		function dentalpress_vc_before_init() {
			if(function_exists('vc_manager')){
				vc_manager()->setAsNetworkPlugin(true);
			}
			
			if(function_exists('vc_set_as_theme')){
				vc_set_as_theme(true);
			}
		}

		add_action( 'init', 'dentalpress_vc_before_init' );
	}

	// VC Assets Register
	if ( !function_exists('dentalpress_vc_assets_register') ) {
		function dentalpress_vc_assets_register() {
			// Style
			wp_register_style('dentalpress-vc-assets', DENTALPRESS_THEME_URL .'includes/vc-addons/assets/css/style.css', array(), DENTALPRESS_THEME_VERSION);

			// Icon Picker
			wp_register_script('dentalpress-vc-assets-iconpicker', DENTALPRESS_THEME_URL .'includes/vc-addons/assets/js/iconpicker.min.js', array('jquery'), '1.2.2', true);

			// Script
			wp_register_script('dentalpress-vc-assets', DENTALPRESS_THEME_URL .'includes/vc-addons/assets/js/script.js', array('jquery'), DENTALPRESS_THEME_VERSION, true);
		}
	}

	add_action( 'init', 'dentalpress_vc_assets_register' );

	// VC Assets Enqueue
	if ( !function_exists('dentalpress_vc_assets_enqueue') ) {
		function dentalpress_vc_assets_enqueue() {
			// Styles
			wp_enqueue_style( 'dentalpress-vc-assets' );

			// Scripts
			wp_enqueue_script( 
				array(
					'dentalpress-vc-assets',
					'dentalpress-vc-assets-iconpicker'
				)
			);
		}
	}

